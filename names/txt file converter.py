#               Turns our current data structure of a string 'Name,Sex,Number'
#               Into ['Name','Sex',Number]

def isolate_data(string):
    end_of_name=string.find(',')
    almost_just_name=string[:end_of_name]
    just_name=almost_just_name[1:]
    if just_name[0]=="'":
        just_name=just_name[1:]
    if string.find(',M,')!=-1:
        start_of_number=string.find(',M')
        end_of_number=string.find("'",start_of_number)
        number_string=string[(start_of_number+3):end_of_number]
        just_number=int(number_string)
        gender="M"
    else:
        start_of_number=string.find(',F')
        end_of_number=string.find("'",start_of_number)
        number_string=string[(start_of_number+3):end_of_number]
        just_number=int(number_string)
        gender="F"

    return [just_name, gender, just_number]

